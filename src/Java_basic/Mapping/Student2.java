package Java_basic.Mapping;

public class Student2 {
    private String name;
    private String sss;
    private int age;

    public Student2(String name, String sss, int age) {
        this.name = name;
        this.sss = sss;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String getSss() {
        return sss;
    }

    public int getAge() {
        return age;
    }
}
